public class Ejercicio3 {
    public static void main(String[] args) {
        // TRIANGULO
        int nnn = 15;
        for (int i = 0; i < nnn; i++) {
            for (int j = 0; j < i; j++) {
                System.out.print("*");
            }
            System.out.print("\n");
        }
    }
}
